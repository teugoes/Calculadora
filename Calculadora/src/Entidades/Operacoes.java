package Entidades;

public class Operacoes {
	
	public float adicao (float numA, float numB) {
		float total;
		total = numA + numB;
		return total;
	}
	
	public float subtracao (float numA, float numB) {
		float total;
		total = numA - numB;
		return total;
	}
	
	public float multiplicacao (float numA, float numB) {
		float total;
		total = numA * numB;
		return total;
	}
	
	public float divisao (float numA, float numB) {
		float total;
		total = numA / numB;
		return total;
	}
}
